create table if not exists public.events (
  id bigint generated by default as identity primary key,
  title text not null,
  description text not null,
  date text not null,
  time text not null,
  location text not null,
  image text,
  participants integer not null default 0,
  category text,
  created_at timestamptz not null default now()
);

create table if not exists public.app_users (
  id bigint generated by default as identity primary key,
  display_name text not null,
  email text not null unique,
  pin_hash text not null,
  role text not null default 'user',
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  constraint app_users_role_check check (role in ('user', 'admin'))
);

create table if not exists public.leaderboard (
  id bigint generated by default as identity primary key,
  app_user_id bigint references public.app_users(id) on delete set null,
  player_name text not null,
  score integer not null,
  played_at timestamptz not null default now(),
  week_ref text not null
);

create table if not exists public.arcade_users (
  id bigint generated by default as identity primary key,
  app_user_id bigint unique references public.app_users(id) on delete set null,
  player_name text not null unique,
  xp integer not null default 0,
  arcade_credit integer not null default 0,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists public.arcade_missions (
  id bigint generated by default as identity primary key,
  user_id bigint not null references public.arcade_users(id) on delete cascade,
  mission_key text not null,
  title text not null,
  description text not null,
  current_progress integer not null default 0,
  target integer not null,
  completed boolean not null default false,
  reward_xp integer not null default 0,
  reward_credit integer not null default 0,
  week_ref text not null,
  expires_at timestamptz not null,
  completed_at timestamptz,
  unique(user_id, mission_key, week_ref)
);

create table if not exists public.arcade_coupons (
  id bigint generated by default as identity primary key,
  user_id bigint not null references public.arcade_users(id) on delete cascade,
  code text not null unique,
  discount_type text not null,
  discount_value integer not null,
  status text not null default 'unused',
  event_id bigint references public.events(id) on delete set null,
  source text not null,
  expires_at timestamptz not null,
  created_at timestamptz not null default now(),
  used_at timestamptz
);

create table if not exists public.arcade_attempts (
  id bigint generated by default as identity primary key,
  user_id bigint not null references public.arcade_users(id) on delete cascade,
  app_user_id bigint references public.app_users(id) on delete set null,
  run_id text not null unique,
  score integer not null,
  duration_ms integer not null,
  submitted_at timestamptz not null default now(),
  week_ref text not null
);

create table if not exists public.tournaments (
  id bigint generated by default as identity primary key,
  slug text not null unique,
  name text not null,
  status text not null,
  teams integer not null default 0,
  max_teams integer,
  date_label text not null,
  time text not null,
  location text not null,
  leader text,
  format text not null,
  description text not null,
  accent text,
  bracket jsonb not null default '[]'::jsonb,
  created_at timestamptz not null default now()
);

create table if not exists public.tournament_registrations (
  id bigint generated by default as identity primary key,
  tournament_id bigint not null references public.tournaments(id) on delete cascade,
  app_user_id bigint not null references public.app_users(id) on delete cascade,
  team_name text not null,
  notes text,
  status text not null default 'pending',
  created_at timestamptz not null default now(),
  unique(tournament_id, app_user_id)
);

