create index if not exists events_date_time_idx on public.events(date, time);
create index if not exists leaderboard_week_score_idx on public.leaderboard(week_ref, score desc, played_at asc);
delete from public.leaderboard l
using (
  select
    id,
    row_number() over (
      partition by app_user_id, week_ref
      order by score desc, played_at asc, id asc
    ) as row_rank
  from public.leaderboard
  where app_user_id is not null
) ranked
where l.id = ranked.id
  and ranked.row_rank > 1;
create unique index if not exists leaderboard_app_user_week_unique_idx
  on public.leaderboard(app_user_id, week_ref)
  where app_user_id is not null;
create index if not exists arcade_attempts_user_week_idx on public.arcade_attempts(user_id, week_ref);
create index if not exists arcade_coupons_user_status_idx on public.arcade_coupons(user_id, status);
create index if not exists tournaments_slug_idx on public.tournaments(slug);
create index if not exists tournament_registrations_user_idx on public.tournament_registrations(app_user_id, tournament_id);

